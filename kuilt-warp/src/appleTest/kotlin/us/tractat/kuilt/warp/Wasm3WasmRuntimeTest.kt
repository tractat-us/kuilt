import kotlin.test.assertContentEquals
import kotlin.test.assertFailsWith

/**
 * Integration tests for [Wasm3WasmRuntime]: happy-path execution plus the load-time sandbox
 * guards, mirroring [ChicoryWasmRuntimeTest] over the native wasm3 cinterop.
 *
 * Happy path: `reverse.wasm` exports the warp linear-memory ABI (`memory`, `warp_alloc`,
 * `warp_run`) and reverses its input bytes — proving args marshal into wasm3 linear memory,
 * `warp_run` executes, and the packed result reads back.
 *
 * Load-guard tests verify that [Wasm3WasmRuntime.load] rejects capability-violating modules
 * with [WasmLoadException] before any execution occurs: a declared import, an oversize memory
 * ceiling (declared max or initial), malformed bytes, a missing `warp_alloc`/`warp_run` ABI
 * export, and a module that exports the ABI but no linear memory.
 *
 * Run-guard: a trapping kernel surfaces as [WasmExecutionException]. wasm3 has no safe
 * cross-thread abort surface, so a CPU-bomb execution-timeout bound is deferred (see the
 * [Wasm3WasmRuntime] KDoc); only the load-time guards are asserted here.
 *
 * wasm fixtures are embedded as byte literals — Kotlin/Native has no classpath resource loading,
 * so the same kernels the JVM test reads from `src/jvmTest/resources` are inlined verbatim here.
 *
 * @see Wasm3RuntimeDispatchTest
 */
class Wasm3WasmRuntimeTest {

    private val runtime = Wasm3WasmRuntime()

    // --- Happy-path tests — guards must not over-reject reverse.wasm ---

    @Test
    fun fourBytesReverse() = runTest {
        val op = runtime.load(REVERSE_WASM)
        assertContentEquals(byteArrayOf(4, 3, 2, 1), op.invoke(byteArrayOf(1, 2, 3, 4)))
    }

    @Test
    fun emptyInputReturnsEmpty() = runTest {
        val op = runtime.load(REVERSE_WASM)
        assertContentEquals(byteArrayOf(), op.invoke(byteArrayOf()))
    }

    @Test
    fun asciiPayloadRoundTripsReversed() = runTest {
        val op = runtime.load(REVERSE_WASM)
        val input = "Hello, warp!".encodeToByteArray()
        val expected = input.reversedArray()
        val first = op.invoke(input)
        val second = op.invoke(input)
        assertAll(
            { assertContentEquals(expected, first) },
            { assertContentEquals(expected, second, "second invoke reuses the same instance") },
        )
    }

    // --- Load-guard tests ---

    @Test
    fun loadRejectsModuleWithImports() {
        assertFailsWith<WasmLoadException> { runtime.load(IMPORTS_WASM) }
    }

    @Test
    fun loadRejectsModuleWithOversizeMemory() {
        assertFailsWith<WasmLoadException> { runtime.load(BIGMEM_WASM) }
    }

    @Test
    fun loadRejectsModuleWithOversizeInitialMemory() {
        assertFailsWith<WasmLoadException> { runtime.load(LARGEINIT_WASM) }
    }

    @Test
    fun loadRejectsMalformedBytes() {
        assertFailsWith<WasmLoadException> { runtime.load(byteArrayOf(0x00, 0x61, 0x73, 0x6d, 0x09)) }
    }

    @Test
    fun loadRejectsModuleMissingAbiExports() {
        assertFailsWith<WasmLoadException> { runtime.load(NOABI_WASM) }
    }

    @Test
    fun loadRejectsModuleMissingMemoryExport() {
        assertFailsWith<WasmLoadException> { runtime.load(NOMEMORY_WASM) }
    }

    // --- Run-guard test ---

    @Test
    fun trapSurfacesAsExecutionException() = runTest {
        assertFailsWith<WasmExecutionException> { runtime.load(TRAP_WASM).invoke(ByteArray(0)) }
    }
}

