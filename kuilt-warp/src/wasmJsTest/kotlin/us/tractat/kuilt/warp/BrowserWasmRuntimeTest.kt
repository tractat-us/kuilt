import kotlin.test.assertContentEquals
import kotlin.test.assertFailsWith

/**
 * Guard + happy-path tests for [BrowserWasmRuntime], mirroring the JVM `ChicoryWasmRuntimeTest`
 * load-guard suite over the browser WebAssembly JS API.
 *
 * The same `.wat` provenance as the JVM `jvmTest/resources` `.wasm` fixtures — embedded here as
 * `byteArrayOf` literals because classpath resources are not available in the browser (same
 * approach as [BrowserWasmSquareOp]'s `SQUARE_WASM`).
 *
 * Load-guard tests verify that [BrowserWasmRuntime.load] rejects capability-violating, oversize,
 * malformed, or ABI-incomplete modules with [WasmLoadException] before any execution occurs — so a
 * verified-but-broken kernel is a TERMINAL load failure, never a raw JS error that escapes the
 * contract and triggers an anti-entropy retry storm.
 */
class BrowserWasmRuntimeTest {

    private val runtime = BrowserWasmRuntime()

    // --- Load-guard tests ---

    @Test
    fun loadRejectsModuleWithImports() {
        assertFailsWith<WasmLoadException> { runtime.load(IMPORTS_WASM) }
    }

    @Test
    fun loadRejectsModuleWithOversizeMemory() {
        assertFailsWith<WasmLoadException> { runtime.load(BIGMEM_WASM) }
    }

    @Test
    fun loadRejectsModuleWithOversizeInitialMemory() {
        assertFailsWith<WasmLoadException> { runtime.load(LARGEINIT_WASM) }
    }

    @Test
    fun loadRejectsMalformedBytes() {
        assertFailsWith<WasmLoadException> { runtime.load(byteArrayOf(0, 1, 2, 3, 4, 5, 6, 7, 8)) }
    }

    @Test
    fun loadRejectsModuleMissingAbiExports() {
        assertFailsWith<WasmLoadException> { runtime.load(NOABI_WASM) }
    }

    @Test
    fun loadRejectsModuleMissingMemoryExport() {
        assertFailsWith<WasmLoadException> { runtime.load(NOMEMORY_WASM) }
    }

    // --- Happy-path tests — guards must not over-reject reverse.wasm ---

    @Test
    fun fourBytesReverse() = runTest {
        val op = runtime.load(REVERSE_WASM)
        assertContentEquals(byteArrayOf(4, 3, 2, 1), op.invoke(byteArrayOf(1, 2, 3, 4)))
    }

    @Test
    fun emptyInputReturnsEmpty() = runTest {
        val op = runtime.load(REVERSE_WASM)
        assertContentEquals(byteArrayOf(), op.invoke(byteArrayOf()))
    }

    @Test
    fun asciiPayloadRoundTripsReversed() = runTest {
        val op = runtime.load(REVERSE_WASM)
        val input = "Hello, warp!".encodeToByteArray()
        val expected = input.reversedArray()
        val first = op.invoke(input)
        val second = op.invoke(input)
        assertAll(
            { assertContentEquals(expected, first) },
            { assertContentEquals(expected, second, "second invoke reuses the same instance") },
        )
    }
}

